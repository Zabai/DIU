    private void ButtonRegisterOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonRegisterOKActionPerformed
        // TODO add your handling code here:
        password = String.valueOf(TextNewPassword1.getText());
        String check = String.valueOf(TextNewPassword2.getText());
        
        TextNewPassword1.setText(null);
        TextNewPassword2.setText(null);
        
        if(password.equals(check)){
            registered = true;
            
            TextRegistered.setText("Sí");
            
            DialogRegister.setVisible(false);
            
            ButtonRegister.setEnabled(false);
            ButtonLogIn.setEnabled(true);
        }else{
            JOptionPane.showMessageDialog(null,
                    "Las contraseñas son diferentes\nIntentalo de nuevo",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ButtonRegisterOKActionPerformed

