    private void ButtonLogInOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonLogInOKActionPerformed
        // TODO add your handling code here:
        String check = String.valueOf(TextLogInPassword.getText());
        
        TextLogInPassword.setText(null);
        
        if(tries == 3){
            JOptionPane.showMessageDialog(null,
                    "Has llegado al nº máximo de intentos",
                    "Salir", JOptionPane.OK_OPTION);
            System.exit(1);
        }
        if(check.equals(password)){
            logged = true;
            tries = 0;
            
            TextLogged.setText("Sí");
            
            DialogLogIn.setVisible(false);
            
            ButtonLogIn.setText("Cerrar sesión");
            
            ButtonRegister.setEnabled(true);
            ButtonRegister.setText("Cambiar contraseña");
        }else{
            JOptionPane.showMessageDialog(null,
                    "Contraseña incorrecta\nIntentalo de nuevo", 
                    "Error", JOptionPane.ERROR_MESSAGE);
            tries++;
        }
    }//GEN-LAST:event_ButtonLogInOKActionPerformed

    private void ButtonChangeOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonChangeOKActionPerformed
        // TODO add your handling code here:
        String actualPass = TextChangePassword1.getText();
        String newPass = TextChangePassword2.getText();
        
        TextChangePassword1.setText(null);
        TextChangePassword2.setText(null);
        
        if(actualPass.equals(password)){
            password = newPass;
            DialogChangePassword.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(null,
                    "La contraseña actual introducida no es correcta",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ButtonChangeOKActionPerformed

