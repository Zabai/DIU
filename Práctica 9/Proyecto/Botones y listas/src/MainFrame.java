    private void ButtonDeleteSelectionList2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonDeleteSelectionList2ActionPerformed
        // TODO add your handling code here:
        List selected = List2.getSelectedValuesList();
        if(selected.isEmpty()) return;
        
        List2.setSelectionInterval(0, List2.getModel().getSize()-1);
        List totalList = List2.getSelectedValuesList();
        totalList.removeAll(selected);
        List2.setListData((String[]) totalList.toArray(new String[0]));
    }//GEN-LAST:event_ButtonDeleteSelectionList2ActionPerformed

