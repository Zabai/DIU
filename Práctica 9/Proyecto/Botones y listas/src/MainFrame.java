    private void ButtonToList1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonToList1ActionPerformed
        // TODO add your handling code here:
        String data = TextData.getText();
        if(data == null || data.isEmpty()) return;
        TextData.setText("");
        
        if(List1.getModel().getSize() == 0){
            String[] values = new String[1];
            values[0] = data;
            List1.setListData(values);
        }else{
            List1.setSelectionInterval(0, List1.getModel().getSize()-1);
            List values = List1.getSelectedValuesList();
            values.add(data);
            List1.setListData((String[]) values.toArray(new String[0]));
        }
    }//GEN-LAST:event_ButtonToList1ActionPerformed

